
package colecciones;

// Definir la clase Producto
public class Productos {
    private String nombre;
    private double precio;
    
    // Constructor
    public Productos(String nombre, double precio) {
        this.nombre = nombre;
        this.precio = precio;
    }

    // Getters
    public String getNombre() {
        return nombre;
    }

    public double getPrecio() {
        return precio;
    }
}
    